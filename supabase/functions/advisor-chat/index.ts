import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.57.4";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, insights, summary } = await req.json();
    
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const authHeader = req.headers.get("Authorization");
    if (!authHeader) {
      throw new Error("Missing authorization header");
    }

    const supabaseUrl = Deno.env.get("SUPABASE_URL")!;
    const supabaseKey = Deno.env.get("SUPABASE_ANON_KEY")!;
    const supabase = createClient(supabaseUrl, supabaseKey, {
      global: { headers: { Authorization: authHeader } },
    });

    const { data: { user }, error: userError } = await supabase.auth.getUser();
    if (userError || !user) {
      throw new Error("Unauthorized");
    }

    // Build context-aware system prompt
    const systemPrompt = `You are an AI music career advisor for a music industry simulation game. You provide strategic, data-driven advice to help players grow their music careers.

${insights && insights.suggestions?.length > 0 ? `
CURRENT INSIGHTS:
${insights.suggestions.map((s: any) => `- ${s.title}: ${s.message}`).join('\n')}
` : ''}

${summary ? `
PLAYER STATS (Last 7 Days):
- Total Streams: ${summary.totalStreams7Days?.toLocaleString() || 0}
- Revenue: $${summary.totalRevenue7Days?.toLocaleString() || 0}
- Listeners: ${summary.listenerReach7Days?.toLocaleString() || 0}
${summary.topMomentumTrack ? `- Top Track: "${summary.topMomentumTrack.title}" (${summary.topMomentumTrack.currentStreams} streams, ${(summary.topMomentumTrack.growthRate * 100).toFixed(0)}% growth)` : ''}
` : ''}

Guidelines:
- Keep responses concise and actionable (2-3 sentences max)
- Focus on specific game mechanics: releases, gigs, rehearsals, marketing, equipment
- Use music industry terminology but keep it accessible
- Reference the player's actual data when possible
- Suggest concrete next steps they can take in the game
- Be encouraging but realistic about challenges
- When data is limited, focus on foundational strategies`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits depleted. Please add credits in Settings." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI service unavailable" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Advisor chat error:", error);
    return new Response(
      JSON.stringify({ 
        error: error instanceof Error ? error.message : "Unknown error" 
      }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
